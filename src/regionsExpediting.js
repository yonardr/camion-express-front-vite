export const REGION_CITIES = Object.keys(REGIONS_EXPEDITING)

export function calcRegionExpediting(city, weight, volume) {
    const row = REGIONS_EXPEDITING[city]
    if (!row) return 0
    const w = Number(weight) || 0, v = Number(volume) || 0
    let wi = REGION_WEIGHT_BOUNDS.findIndex(b => w <= b)
    let vi = REGION_VOLUME_BOUNDS.findIndex(b => v <= b)
    if (wi < 0) wi = row.length - 1
    if (vi < 0) vi = row.length - 1
    const idx = Math.min(Math.max(wi, vi), row.length - 1)
    return row[idx] || 0
}
